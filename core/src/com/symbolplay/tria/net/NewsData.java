package com.symbolplay.tria.net;

public final class NewsData {
    
    private int index;
    private String date;
    private String title;
    private String image;
    private String generalLink;
    private String androidLink;
    private String text;
    
    public NewsData() {
    }
    
    public int getIndex() {
        return index;
    }
    
    public String getDate() {
        return date;
    }
    
    public String getTitle() {
        return title;
    }
    
    public String getImage() {
        return image;
    }
    
    public String getGeneralLink() {
        return generalLink;
    }
    
    public String getAndroidLink() {
        return androidLink;
    }
    
    public String getText() {
        return text;
    }
}
